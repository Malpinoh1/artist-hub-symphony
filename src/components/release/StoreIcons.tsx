// Generic icon for stores without brand SVGs
function GenericStoreIcon({ name }: { name: string }) {
  const firstLetter = name.charAt(0).toUpperCase();
  return (
    <div className="w-5 h-5 rounded bg-muted flex items-center justify-center">
      <span className="text-[10px] font-bold text-muted-foreground">{firstLetter}</span>
    </div>
  );
}

export function getStoreIcon(storeName: string): React.ReactNode {
  const entry = storeIconMap[storeName];
  if (entry) return entry.icon;
  
  // Special case mappings
  switch (storeName) {
    case 'KKBOX':
    case 'NetEase':
    case 'Tencent':
    case 'Saavn':
    case 'JOOX':
    case 'Trebel':
    case 'Qobuz':
    case 'FLO':
    case 'Rythm':
    case 'Lissen':
    case 'Fizy':
    case 'Kuack':
    case 'Claro Música':
      return <GenericStoreIcon name={storeName} />;
    case 'SoundExchange':
      return <Radio className="w-5 h-5 text-muted-foreground" />;
    case 'iTunes Ringtones':
      return <Smartphone className="w-5 h-5 text-muted-foreground" />;
    case 'Claro Ringtones':
    case 'Algar':
      return <Smartphone className="w-5 h-5 text-muted-foreground" />;
    default:
      return <Globe className="w-5 h-5 text-muted-foreground" />;
  }
}

export function StoreStatusBadge({ status }: { status: string }) {
  const colorMap: Record<string, string> = {
    delivered: 'bg-green-100 text-green-700 dark:bg-green-900/30 dark:text-green-400',
    incomplete: 'bg-amber-100 text-amber-700 dark:bg-amber-900/30 dark:text-amber-400',
    pending: 'bg-muted text-muted-foreground',
  };
  return (
    <span className={`text-[10px] px-1.5 py-0.5 rounded-full font-medium capitalize ${colorMap[status] || colorMap.pending}`}>
      {status}
    </span>
  );
}
